	assert.Equal(t, "ও", MustParse("w"))
	assert.Equal(t, "ওয়া", MustParse("wa"))
	assert.Equal(t, "-ওয়া-", MustParse("-wa-"))
	assert.Equal(t, "ওয়ু", MustParse("woo"))
